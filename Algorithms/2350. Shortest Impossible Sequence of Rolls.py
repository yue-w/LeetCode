from typing import List
class Solution:
    def shortestSequence(self, rolls: List[int], k: int) -> int:
        rst = 0
        seen = set()
        for n in rolls:
            seen.add(n)
            if len(seen) == k:
                seen = set()
                rst += 1
        
        
        return rst + 1
            
